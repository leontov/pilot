#ifndef TFIDF_H
#define TFIDF_H

// Быстрая косинусная мера на базе токенизации (не зависит от внешних библиотек)
// Возвращает значение в диапазоне [0,1]. Чем выше — тем более похожи строки.
// Поддерживает UTF-8 в смысле: не ломает многобайтовые токены — токены выделяются
// по ASCII-разделителям (пробел, пунктуация). Для русских слов сравнение
// будет по точному байтовому совпадению.

double tfidf_cosine_similarity(const char* a, const char* b);

#endif // TFIDF_H
